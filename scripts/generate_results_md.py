#!/usr/bin/env python3
"""Generate BENCHMARK_RESULTS.md from scalar_vs_amx CSV output.

Usage:
    python3 scripts/generate_results_md.py benchmark-results/scalar-vs-amx-*.csv
"""

import csv
import sys
import pathlib
from datetime import datetime, timezone

ROOT = pathlib.Path(__file__).resolve().parents[1]


def load_csv(paths):
    rows = []
    for p in paths:
        with open(p) as f:
            rows.extend(csv.DictReader(f))
    for r in rows:
        for k in ("m", "k", "n"):
            r[k] = int(r[k])
        for k in ("flops", "scalar_us", "amx_us", "amx_par_us", "blas_us",
                   "scalar_gflops", "amx_gflops", "amx_par_gflops", "blas_gflops"):
            r[k] = float(r[k])
    return rows


def fmt(v, decimals=1):
    if v < 0.01:
        return f"{v:.3f}"
    if v < 1:
        return f"{v:.2f}"
    if v < 100:
        return f"{v:.{decimals}f}"
    return f"{v:.0f}"


def speedup(a, b):
    if b <= 0:
        return "—"
    return f"{a/b:.1f}×"


def main():
    csv_files = sys.argv[1:] if len(sys.argv) > 1 else sorted(
        str(p) for p in ROOT.glob("benchmark-results/scalar-vs-amx-*.csv")
    )
    if not csv_files:
        print("No CSV files found.", file=sys.stderr)
        sys.exit(1)

    rows = load_csv(csv_files)
    chip = rows[0]["chip"] if rows else "Unknown"
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    lines = []
    lines.append(f"<!-- Auto-generated by CI — do not edit manually -->")
    lines.append(f"<!-- Last updated: {now} -->")
    lines.append(f"<!-- Chip: {chip} -->")
    lines.append("")
    lines.append(f"> Measured on **{chip}** · f32 · single thread · {now}")
    lines.append("")

    # Square matmul
    sq = [r for r in rows if r["op"] == "matmul" and r["m"] == r["k"] == r["n"]]
    target = {8, 16, 32, 64, 128, 256, 512, 1024}
    sq_sub = [r for r in sq if r["m"] in target]

    lines.append("#### Square Matmul (N×N × N×N)")
    lines.append("")
    lines.append("| N | Scalar (µs) | AMX (µs) | AMX-par (µs) | Accel (µs) | Scalar GF | AMX GF | AMX-par GF | Accel GF |")
    lines.append("|--:|------------:|--------:|------------:|-----------:|----------:|------:|-----------:|--------:|")
    for r in sq_sub:
        lines.append(
            f"| {r['m']} "
            f"| {fmt(r['scalar_us'])} "
            f"| {fmt(r['amx_us'])} "
            f"| {fmt(r['amx_par_us'])} "
            f"| {fmt(r['blas_us'])} "
            f"| {fmt(r['scalar_gflops'])} "
            f"| {fmt(r['amx_gflops'])} "
            f"| {fmt(r['amx_par_gflops'])} "
            f"| {fmt(r['blas_gflops'])} |"
        )
    lines.append("")

    # Peak summary
    best_amx = max((r for r in sq), key=lambda r: r["amx_gflops"])
    best_par = max((r for r in sq), key=lambda r: r["amx_par_gflops"])
    best_blas = max((r for r in sq), key=lambda r: r["blas_gflops"])
    lines.append(
        f"> **Peak AMX (1 core): {fmt(best_amx['amx_gflops'])} GFLOPS** · "
        f"**Peak AMX-parallel: {fmt(best_par['amx_par_gflops'])} GFLOPS** · "
        f"**Peak Accelerate: {fmt(best_blas['blas_gflops'])} GFLOPS**"
    )
    lines.append("")

    # Rectangular matmul
    rect = [r for r in rows if r["op"] == "matmul" and not (r["m"] == r["k"] == r["n"])]
    # Pick interesting shapes
    interesting = {"16x256x256", "32x768x768", "128x768x3072", "100x200x150",
                   "37x53x41", "256x16x256", "1x4096x4096", "32x4096x4096"}
    rect_sub = [r for r in rect if r["shape"] in interesting]

    if rect_sub:
        lines.append("#### Rectangular Matmul (selected shapes)")
        lines.append("")
        lines.append("| Shape | Scalar (µs) | AMX (µs) | Accel (µs) | AMX GFLOPS | Accel GFLOPS |")
        lines.append("|------:|------------:|--------:|-----------:|----------:|-----------:|")
        for r in rect_sub:
            shape = r["shape"].replace("x", "×")
            lines.append(
                f"| {shape} "
                f"| {fmt(r['scalar_us'])} "
                f"| {fmt(r['amx_us'])} "
                f"| {fmt(r['blas_us'])} "
                f"| {fmt(r['amx_gflops'])} "
                f"| {fmt(r['blas_gflops'])} |"
            )
        lines.append("")

    # Dot product
    dot = [r for r in rows if r["op"] == "dot"]
    dot_sub = [r for r in dot if r["m"] in {64, 1024, 16384, 65536, 1048576}]

    if dot_sub:
        lines.append("#### Dot Product (a · b)")
        lines.append("")
        lines.append("| Length | Scalar (µs) | AMX (µs) | Accel (µs) | Scalar GFLOPS | AMX GFLOPS | Accel GFLOPS |")
        lines.append("|-------:|------------:|--------:|-----------:|--------------:|----------:|-----------:|")
        for r in dot_sub:
            length = r["shape"].replace("len=", "")
            lines.append(
                f"| {length} "
                f"| {fmt(r['scalar_us'], 2)} "
                f"| {fmt(r['amx_us'], 2)} "
                f"| {fmt(r['blas_us'], 3)} "
                f"| {fmt(r['scalar_gflops'])} "
                f"| {fmt(r['amx_gflops'])} "
                f"| {fmt(r['blas_gflops'])} |"
            )
        lines.append("")

    md = "\n".join(lines) + "\n"
    out = ROOT / "BENCHMARK_RESULTS.md"
    out.write_text(md)
    print(f"Wrote {out}")


if __name__ == "__main__":
    main()
